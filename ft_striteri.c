#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	i = 0;
	if (!s)
		return ;
	while (i < (unsigned int)ft_strlen(s))
	{
		(*f)(i, &s[i]);
		i++;
	}
}
